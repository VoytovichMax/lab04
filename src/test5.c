#include <stdio.h>
int main()
{
	double x, y; //Змінні
	x = -1;
	if (x > 1) //Якщо х більше 1
	{
		y = x - 1;
	}
	else if (x < -1) //Якщо х меньше -1
	{
		x = -1 * x;
		y = x - 1;
	}
	else //Для х від -1 до 1
	{
		if (x < 0) //Якщо х меньше 0, тодiйого треба зробити позитивним
		{
			x = -1 * x;
		}
		y = 1 * (1 - x);
	}
}
